#Python if Statement

number = 10
# check if number is greater than 0
if number > 0:
    print('Number is positive.')

#Python if...else Statement
number = 10

if number > 0:
    print('Positive number')

else:
    print('Negative number')

#Python if...elif...else Statement
number = 0

if number > 0:
    print("Positive number")

elif number == 0:
    print('Zero')
else:
    print('Negative number')
